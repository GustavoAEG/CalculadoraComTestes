class Calculadora {


fun Soma(num1: Int, num2:Int): Int{

    return num1 + num2

}

    fun Subtr(num1: Int, num2: Int): Int{

        return num1 - num2

    }

    fun Mult(num1: Int, num2: Int): Int{

        return num1 * num2
    }
    fun Div(num1: Int, num2: Int): Int{

        return num1 / num2
    }

}